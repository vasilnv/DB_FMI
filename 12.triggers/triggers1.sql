CREATE TRIGGER add_passengers ON BOOKING
AFTER INSERT
AS
	UPDATE FLIGHT
	SET NUM_PASS += 1
	WHERE FNUMBER IN (SELECT FLIGHT_NUMBER FROM inserted)

	UPDATE AGENCY	
	SET NUM_BOOK += 1
	WHERE AGENCY.NAME IN (SELECT AGENCY FROM inserted)

